function [ ShortSymbols ] = Generate_ShortSymbols
%
% Генерирует часть преамбулы (short symbols) во временной области
% согласно стандарту 802.11a
%
% См. "IEEE Std 802.11-2012", стр. 1592, 2538
%
% ВНИМАНИЕ! В данной функции генерируется не 161 комлпексных отсчётов
% (как указано в стандарте), а 160 комплексных отсчётов.
% Также отсутсвует умножение на оконную функцию.
%
% На выходе получаем 10 одинаковых Short Symbol
% по 16 комплексных отсчётов каждый

        % S_-26_26
        S = sqrt(13/6) * [0, 0, 1+1i, ...
                0, 0, 0, -1-1i, ...
                0, 0, 0,  1+1i, ...
                0, 0, 0, -1-1i, ...
                0, 0, 0, -1-1i, ...
                0, 0, 0,  1+1i, ...
                0, 0, 0, 0, 0, 0, 0, -1-1i, ...
                0, 0, 0, -1-1i, ...
                0, 0, 0,  1+1i, ...
                0, 0, 0,  1+1i, ...
                0, 0, 0,  1+1i, ...
                0, 0, 0,  1+1i, ...
                0, 0];
        
        % Нормальный порядок + Nulls для IFFT блока
        S = [0, ...
             S(28 : 53), ...
             zeros(1, 11), ...
             S(1 : 26)];
     
        ShortSymbols = ifft(S);
        
        % Периодически расширяем, чтобы получить 160 отсчётов
        ShortSymbols = [ShortSymbols, ShortSymbols, ShortSymbols(1 : 32)];
        
end

